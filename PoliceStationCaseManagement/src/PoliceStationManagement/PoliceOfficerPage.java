    private void OfficerSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OfficerSubmitButtonActionPerformed
        // TODO add your handling code here:
        ConnectMSSQL cnObj = new ConnectMSSQL();
        cnObj.connectDB();
        
        String query = "INSERT INTO PoliceOfficer(PoliceOfficerName,Designation,Area,CaseHandledNo,PhoneNo,Address,DutyHour,YearsOfExperience) VALUES(?,?,?,?,?,?,?,?)";
        PreparedStatement pst;
        try {
            pst = (PreparedStatement) cnObj.connection.prepareStatement(query);
            pst.setString(1,OfficerNameTextField.getText());
            pst.setString(2,DesignationTextField.getText());
            pst.setString(3,OfficerAreaTextField.getText() );
            pst.setString(4,CaseHandledTextField.getText() );
            pst.setString(5,OfficerPhoneTextField.getText());
            pst.setString(6,OfficerAddressTextField.getText());
            pst.setString(7,DutyhrTextField.getText());
            pst.setString(8,ExperienceTextField.getText());
            
            
            if((OfficerNameTextField.getText().equals("")) || (DesignationTextField.getText().equals("")) || (OfficerAreaTextField.getText().equals("")) || (OfficerPhoneTextField.getText().equals(""))){
                JOptionPane.showMessageDialog(null,"Please fill up the required(*) text fields!!");
            }
            else{
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null,"Inserted SUCCESSFULLY");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(VictimPage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_OfficerSubmitButtonActionPerformed

